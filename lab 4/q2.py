"""Dada a string "Python é uma linguagem de programação", imprima somente a palavra "linguagem"."""

# String fornecida
texto = "Python é uma linguagem de programação"

# Dividindo a string em palavras e acessando a palavra 'linguagem'
palavras = texto.split()
print(palavras[3])  # A palavra "linguagem" está na posição 3
